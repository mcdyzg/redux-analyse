/**
大体流程：
    发布-订阅模式，每次执行subscribe方法将会注册订阅事件，每次dispatch会执行所有的订阅事件，脏检查。

细致流程：
    1. 执行createStore()方法，生成currentState(对象),currentListeners(数组)两个变量，这两个变量会一直保存在内存中供调用，currentState变量保存redux状态树，currentListeners保存当前注册的所有订阅函数。同时生成dispatch，subscribe，getState等方法。
    2. 每次执行subscribe(listener)，会将listener方法添加到currentListeners数组中。
    3. 每次执行dispatch(action)方法时，首先会拿到用户写的reducer方法，传入currentState和action并执行，然后将结果重新赋给currentState。然后遍历currentListeners，执行每个回调。
    (对于异步action,如果redux-thunk中间件检测到action不是一个object而是function，那么不会触发dispatch，而是将dispatch方法传到action方法里，并执行action,由action决定在何时触发dispatch)

使用场景：
    一般会在componentDidMount方法里先将store绑定到当前组件的state上，然后执行subscribe方法，将更新state的方法注册到Listeners数组里
    componentDidMount(){
		let store = this.context.store
		this.updateStore()
		this.unsubscribe = store.subscribe(()=>{
			console.log('list1 触发回调')
			this.updateStore()
		})
	}
	updateStore=()=>{
		let store = this.context.store
		this.setState({
			...this.state,
			...store.getState(),
		})
	}
    componentWillUnmount(){
		console.log('list1 消失')
		this.unsubscribe()
	}
    然后每当执行this.context.store.dispatch({
        type:'add',
        data:1,
    })时，this.updateStore()方法就会执行，然后updateStore会将最新的store.getState()绑定到当前state上。
*/


